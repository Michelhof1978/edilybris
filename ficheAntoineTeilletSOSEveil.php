                    <p class="">
                    Conquête politique, emprise financière

Dans mon précédent ouvrage, j’ai évoqué de nombreuses zones à défendre et des projets détraquant les droits inaliénables de l’Homme, notamment en France. Ces faits révèlent une défaite politique sur la mainmise économique des groupes industriels. L’actionnaire a plus d’impact que le normalien. Le patronat garde et conforte son pouvoir en soutenant et en finançant les campagnes de certains candidats. Nous avons la possibilité de manifester notre colère, notre dégoût vis-à-vis des technocrates, en permanent décalage avec les citoyens.

Des troubles fonctionnels s’emparent de ce rôle de l’État et des collectivités puisque ces derniers collaborent au capitalisme mondial et accroissent les écarts entre petites mains et grandes paluches argentées. Cela provoque une crise généralisée, institutionnelle, financière, économique, sociale, judiciaire. Certains politiques se servent de cette crise pour gagner en popularité. Ils jouent sur les sentiments de peur, d’insécurité, de perspectives moroses. C’est d’ailleurs un calamiteux phénomène, car les aboyeurs charment les électeurs en déblatérant des propositions simplistes, rassurantes, sans véritables visions. Et cette mécanique profondément humaine fonctionne.

Le subterfuge s’amplifie quand on sait que cette minorité au pouvoir serre la main aux moteurs du libre-échange. Elle ne veut plus affronter les marchés financiers et les injustices satellites. Elle les accompagne et un accord à l’amiable s’amorce officieusement derrière un illusoire front populaire. À coups de messages d’admiration pour Jean Jaurès, un deal s’opère entre les membres de la politique institutionnelle et les puissants actionnaires. Je te laisse ta place avec le bénéfice d’une belle marge de manœuvre, tu me soutiens et vice versa.

L’État français, depuis les années 1970, a mené les banques privées au tout premier plan, reléguant dans les tribunes les caisses centrales. Outre leurs abus accordant des financements de campagnes électorales, c’est aussi une manière de confirmer le capitalisme à la barre du navire. Les institutions fiduciaires ne font qu’encourager les hommes politiques sur le chemin de l’argent. Suivre le vent en poupe, s’aligner sur la direction ultralibérale, là résident les engagements officieux de nos gouvernants.<br>
                    </p>
