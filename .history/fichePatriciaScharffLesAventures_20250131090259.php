                    <h4 class="h2Jaune text-center m-3">PROLOGUE</h4>
                    <p>
    Mael est un petit garçon de cinq ans et il entend souvent dire qu’il est grand pour son âge, mais bientôt ce ne sera certainement plus vrai, car il va changer d’âge et avoir six ans.<br>
    <img src="images/patriciaScharff/lesAventures1.webp" class="img-fluid mx-auto d-block" alt="Image 1"><br>
    Ça s’appelle un anniversaire et c’est l’occasion de faire un goûter avec les camarades de classe qui vont venir avec des bonbons et des petits cadeaux.<br>
    C’est aussi l’occasion d’inviter la famille le dimanche suivant.<br> 
    Les personnes de la famille arrivent par petits groupes à quelques minutes d’intervalle et elles font beaucoup de bruit pour se dire bonjour.<br>
    Elles disent très fort : « Ah, Mael ! » comme si elles étaient étonnées de le voir alors qu’elles sont venues pour lui.<br>
    Elles déposent des paquets cadeaux près du canapé, mais il ne faut pas y toucher jusqu’au gâteau d’anniversaire.<br>
    Et pourtant, ils font bien envie tous ces paquets recouverts de papier coloré !<br>
    Mais c’est bien d’attendre pour les ouvrir, car jusque-là, on peut tout imaginer, même le super robot qui se transforme en engin spatial ou bien la panoplie tant convoitée.<br>
    Et quand tout le monde est arrivé, c’est l’apéritif.<br>
    L’apéritif consiste à donner du jus de fruits aux enfants et des boissons qui ne sont pas pour les enfants aux adultes.<br>
    Sur la table du salon, il y a des assiettes sur lesquelles se trouvent de petites tartines avec de jolies couleurs, des petits choux salés, de petites tomates qui ont bon goût et des carrés de fromage, mais il ne faut pas en manger trop sinon on n’aura plus faim pour après.<br>
    Et ça pose un problème à Mael qui aime bien toutes ces petites bouchées qui font plaisir et il ne comprend pas bien pourquoi il faut se limiter et avoir faim après.<br>
    Les personnes de la famille discutent par petits groupes, rient et ont le droit de parler la bouche pleine.<br>
    Parmi tous les invités, l’invitée préférée de Mael, c’est sa cousine Clémentine.<br>
    Une cousine, c’est une petite fille qui appelle votre papa « Tonton » et votre maman « Tata » ou « Tatie ».<br>
    On fait la même chose avec son papa et avec sa maman.<br>
    <img src="images/patriciaScharff/lesAventures2.webp" class="img-fluid mx-auto d-block" alt="Image 2"><br>
    Mael avait un tout petit peu plus d’un an quand sa cousine Clémentine est née et il ne se souvient pas de la vie avant Clémentine.<br>
    Elle a d’abord été sans intérêt, car elle ne parlait pas, elle faisait des sourires ou elle pleurait avec une voix rauque.<br>
    Elle faisait aussi des bulles et elle régurgitait un peu de lait caillé et aigre.<br>
    Et puis, elle a essayé de parler, mais ça ne voulait rien dire, même si elle prenait un air sérieux.<br>
    En tout cas, il ne fallait surtout pas lui donner de trop petits jouets, car elle risquait de les avaler.<br>
    C’était d’ailleurs une bonne excuse pour ne pas lui prêter certains jouets que Mael tenait à garder pour lui.<br>
    La phrase magique : « Clémentine est trop petite ! » faisait toujours son petit effet et permettait à Mael de se sentir grand.<br>
    Maintenant, Clémentine a déjà eu des anniversaires et elle sait parler.<br>
    Parfois, elle dit certains mots en riant, en mettant la main devant sa bouche toute rose et en plissant ses yeux bleus comme des fleurs.<br>
    On dit que ce sont des mots appris à l’école, mais en fait ils ont été appris dans la cour de récréation de l’école.<br>
    Il y a toujours des élèves qui connaissent des mots drôles, qui sonnent bien et qu’on ne doit pas répéter à la maison sous peine de se faire punir.<br>
    Clémentine est blonde avec des cheveux très longs rassemblés selon les jours, en nattes, en couettes ou en queue de cheval.<br>
    On peut dire aussi en queue de poney, car Boni, au centre équestre, a une jolie queue blonde qu’il faut brosser avant la promenade.<br>
    Clémentine aime bien jouer à la princesse, mais elle aime aussi jouer aux petites voitures.<br>
    Elle invente des chansons et fait de très beaux dessins de la main gauche alors que sa main droite est posée à plat sur la feuille.<br>
    C’est peut-être pour ça que Clémentine écrit à l’envers.<br>
</p>

