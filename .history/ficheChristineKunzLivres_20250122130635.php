        <h2 class="h2Jaune">Une femme poète à la rochelle</h2>
<p class="text-center">Rentrer dans l'univers de Christine KUNZ s'avère à la fois enthousiasmant et intrigant : d'une part à cause de son énergie débordante qui nous pousse à franchir les portes entrouvertes de son imaginaire, et d'autre part pour ce mystère jalousement préservé de sa vie familiale.<br>

Parlons donc passion avant tout : l'écriture, qu'elle décrit comme intime, spontanée et thérapeutique, se nourrit des vicissitudes de la société, des injustices et des intolérances des peuples dans leur capacité à vivre en harmonie, de la substance même de l'homme à aimer son prochain lorsqu'il s'agit de désir et de partage, de l'emprise de la musique sur nos habitudes de vie. La liste est loin d'être exhaustive, mais permettra au lecteur de mieux cerner notre nouvelle adhérente.<br>
Parlons aussi de l'expression de ses passions, en commençant par sa rencontre en 2005 avec le slam. Pour Christine, ce fut révélateur, comme l'opportunité de faire "sauter le verrou qui interdisait d'être, de dire, de s'affirmer". Alors, s'enchaînent plusieurs productions, sous l'égide de la première scène ouverte de slam poésie à la Rochelle, au Café des Deux Tours, appelée SLAMALAMER.
Sa présence est également remarquée à plusieurs reprises :
- En 2008, à l'inauguration de l'église Saint-Sauveur et en off à l'occasion des Francofolies de La Rochelle ;
- en 2010, au festival de l'université de La Rochelle, dans le cadre des "Étudiants à l'Affiche" ;
- en 2011, aux Assises de la Culture à Niort ;<br>
- en 2012, au Festival Springtime Delight et en workshop chez Strate College (école de design) ;
- en 2013, interventions régulières dans le milieu scolaire (primaire et secondaire) ;
- en 2014 et 2015, animation d'ateliers d'expression sur le métissage au Museum d'Histoire naturelle de La Rochelle ;
- En 2016, sa soif d'expression l'engage à créer avec deux partenaires le festival "Vibrations poétiques" : la première édition, donnée au Cloître des Ursulines à La Rochelle, est un succès. Il invite le visiteur à s'adonner à l'écriture et à s'amuser avec les différents intervenants.
- En 2017, ses collaborations se multiplient : avec le museum d'Histoire Naturelle, la médiathèque Michel Crépeau, le collège de l'Atlantique, le réseau scolaire de Mireuil... Le projet "STAND UP" va l'amener à remplir une salle de la médiathèque principale, consacrant ainsi son sens de la scène et elle animera un atelier slam à l'occasion du salon du livre féminin. Pour finir cette année riche en événements littéraires, Christine se verra invitée au salon du Livre et des Droits de l'Homme à Saintes.<br>
- En 2018, elle conduit le projet "Paroles d'Humains", en partenariat avec le collectif "Actions Solidaires" : le but est d'enregistrer un album qui regroupe plusieurs écrivants d'origines diverses. Une représentation a été donnée en juin de cette année à la médiathèque Michel Crépeau.
Christine s'associe également à Frank Nelson, un compositeur et bassiste, pour créer "Big Bass Band". En avril, la formation complète sort son premier CD et travaille sur un spectacle qui met en valeur la poésie et la basse. 
En 2019, l'idée de transposer ses textes en un recueil se fait et se matérialise grâce à la rencontre avec notre président, Pierre Brandao. C'est le début d'une belle aventure éditoriale, à laquelle nous vous invitons à participer : "Abreuver le fou qui dort en nos déserts"... Tout un programme que nous vous dévoilons ci-dessous...
</p>
