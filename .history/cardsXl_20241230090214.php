script>
        const groups = ["desktop-group1", "desktop-group2"];
        let currentIndex = 0;

        const backButton = document.getElementById("new-desktop-backButton");
        const nextButton = document.getElementById("new-desktop-nextGroupButton");

        function updateVisibility() {
            groups.forEach((groupId, index) => {
                const group = document.getElementById(groupId);
                group.classList.toggle("new-hidden", index !== currentIndex);
            });

            backButton.disabled = currentIndex === 0;
            nextButton.disabled = currentIndex === groups.length - 1;
        }

        backButton.addEventListener("click", () => {
            if (currentIndex > 0) {
                currentIndex--;
                updateVisibility();
            }
        });

        nextButton.addEventListener("click", () => {
            if (currentIndex < groups.length - 1) {
                currentIndex++;
                updateVisibility();
            }
        });

        updateVisibility();
    </script>
