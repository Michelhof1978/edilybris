<h4 class="h2Jaune text-center m-3 ">CHAPITRE 1</h4>


<p class=" lead text-center">Je sais que je suis étendu sur mon pieu. C’est une certitude parce que je me souviens qu’ils m’ont soulevé à plusieurs. J’en ai vu de toutes les couleurs et j’ai bramé comme un cerf. Enfin, je pense que des sons sont sortis de mon gosier mais dans mon état, allez savoir ! Maintenant, tout va bén. J’ai l’impression que mon corps flotte au-dessus de ma paillasse. Je me sens tellement léger que j’ai l’impression d’être une ajhace[1]. Je me dirige doucement vers un tunnel gris métallisé et je goûte avec délice ces instants maghiques quand j’entends des messes basses.<br>
— Je crois qu’il n’en a plus pour longtemps.
Je reconnais la voix de ma dulcinée vieillissante mais pas celle qui lui répond. C’est pt’ être le draule du voisin de la ferme  des Mulots. Il habite un endroit prédestiné : rongeur campagnard, o lui va comme un gant, avec ses yeux vicieux et  ses petites dents acérées prêtes à la mordrerie de tout ce qu’il trouve. J’ignorais qu’il éprouvait de la sympathie à mon égard, quoique... ce soit plutôt de la curiosité malsaine. S’il ressemble à son père, rén d’étonnant.<br>
– Le docteur a dit qu’il pouvait vivre encore plusieurs jours.<br>
Si je dois vivre dans les conditions actuelles, je suis d’accord !<br>
Leurs goules dau diablle[2] poursuivent.
— Le pov, il a tellement souffert que je lui pardonne car il n’était pas toujours gentil.<br>
— Cela fait combien de temps que vous êtes mariés ?
— Quarante-sept ans dans deux mois.
— C’est un bail !
— Oui, j’ai dû souvent me cramponner.
Leurs clapets se ferment. Je ne sais pas qui discute avec ma vélle et o m’tape sur la coloquinte. A-t-elle besoin, cette prune melée de raconter ma vie ! Moi aussi, je l’ai supportée mais heureusement, j’avais des compensations extérieures. La pov Nicole n’était pas une beauté et avec les années, elle a davantage ressemblé à un bonhomme Michelin qu’à une femme attirante. Il m’aurait fallu une mobylette pour en faire le tour ! Pas étonnant si j’allais voer ailleurs ! De plus, elle était bête comme trente six cochons mais, comme on dit, quand le vin est tiré, il faut le boire…</p><br>

