        <h2 class="h2Jaune">Une femme poète à la rochelle</h2>
<p class="text-center">Rentrer dans l'univers de Christine KUNZ s'avère à la fois enthousiasmant et intrigant : d'une part à cause de son énergie débordante qui nous pousse à franchir les portes entrouvertes de son imaginaire, et d'autre part pour ce mystère jalousement préservé de sa vie familiale.<br>

Parlons donc passion avant tout : l'écriture, qu'elle décrit comme intime, spontanée et thérapeutique, se nourrit des vicissitudes de la société, des injustices et des intolérances des peuples dans leur capacité à vivre en harmonie, de la substance même de l'homme à aimer son prochain lorsqu'il s'agit de désir et de partage, de l'emprise de la musique sur nos habitudes de vie. La liste est loin d'être exhaustive, mais permettra au lecteur de mieux cerner notre nouvelle adhérente.
Parlons aussi de l'expression de ses passions, en commençant par sa rencontre en 2005 avec le slam. Pour Christine, ce fut révélateur, comme l'opportunité de faire "sauter le verrou qui interdisait d'être, de dire, de s'affirmer". Alors, s'enchaînent plusieurs productions, sous l'égide de la première scène ouverte de slam poésie à la Rochelle, au Café des Deux Tours, appelée SLAMALAMER.
Sa présence est également remarquée à plusieurs reprises :
- En 2008, à l'inauguration de l'église Saint-Sauveur et en off à l'occasion des Francofolies de La Rochelle ;
- en 2010, au festival de l'université de La Rochelle, dans le cadre des "Étudiants à l'Affiche" ;
- en 2011, aux Assises de la Culture à Niort ;<br>

</p>
