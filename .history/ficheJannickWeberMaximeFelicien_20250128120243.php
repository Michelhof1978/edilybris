<h4 class="h2Jaune text-center m-3 ">Un aperçu de Maxime-Félicien Denéchaud, mémoire sauvegardée</h4>

<p class=" lead text-center">L’hiver 1941 est arrivé très rapidement. Dès la fin du mois de septembre, la neige avait recouvert le pays. Cependant, grâce au soutien logistique familial, la vie de Maxime s’en est trouvée légèrement transformée. Il possédait maintenant de grosses chaussettes de laine, un bonnet, un gilet, une écharpe et des sur-chaussettes fabriquées par Denise. À partir de cette époque, malgré un quotidien difficile, les morsures du froid lui ont paru moins terribles.<br>
Le travail en forêt a repris et pour s’y rendre, les prisonniers devaient d’abord dégager les chemins. Ils ne se pressaient pas, afin de raccourcir la journée de labeur.
Le gel était préférable à la neige car, malgré les clous que le sabotier enfonçait dans les galoches, il devenait quasiment impossible de circuler. Avec les autres, Maxime restait autour du feu pour jouer aux cartes ou lire. Grâce aux envois des familles, ils ont réussi à se constituer une petite bibliothèque ; mon père passait des heures plongé dans la partie histoire-géographie du Larousse.<br>
Un camarade-prisonnier fabriquait des marionnettes dont l’une représentait Hitler, sans culotte et portant un bonnet d’âne. Bien souvent, cette effigie s’est retrouvée éventrée.
– Ce guignol improvisé nous faisait rire aux éclats, confiait-il. Au stalag, le quotidien était plus difficile à supporter mais les prisonniers bénéficiaient de distractions tels que théâtre, orchestre et rencontres sportives. Nous, on devait se contenter de peu mais la nourriture était plus abondante et variée et on pouvait facilement resquiller, alors, à tout prendre…<br>
André qui comprenait de mieux en mieux l’allemand grâce à ses activités amoureuses, apprit en décembre que les troupes allemandes avaient été stoppées devant Moscou.
– Ils auraient dû se souvenir de Napoléon, énonçait sentencieusement Maxime. Et puis, quel bonheur quand les États-Unis sont entrés en guerre ! On a chanté toute la nuit. Jamais cette bande de fous ne pourrait faire face sur tous les fronts, il suffisait d’attendre !<br>
L’hiver 41/42 fut aussi froid que le précédent et, mis à part quelques petites bouteilles d’alcool envoyées par Denise, il avait dû se contenter de tisane.<br>
– J’en avais marre de leur pisse d’âne alors, j’ai décidé de confectionner un alambic. Ah, que de temps passé mais quelle expérience !<br>
L’opération s’est avérée longue et délicate car se procurer le matériel nécessaire relevait de la haute stratégie. Il adorait m’expliquer sa réalisation.<br>
– J’avais volé une vieille lessiveuse dans laquelle on a versé des patates fermentées. Au centre, un tuyau rouillé formant à son extrémité une sorte de serpentin, descendait sur un ancien pot de chambre, le tout monté sur le poêle chauffé à blanc pendant des heures. En fin de soirée, j’ai entendu Marcel crier : « Maxime, Maxime, ça pisse, ça pisse ! »<br>
Il avait réussi à fabriquer de l’alcool. La soirée a été mémorable … évidemment bien arrosée. Des petites choses comme celle-ci maintenaient son moral à l'optimisme ; il le fallait, car les sujets de mécontentement étaient nombreux et engendraient de longues périodes d’abattement. Par exemple, en hiver, le courrier et les colis parvenaient rarement dans des délais raisonnables et arrivaient souvent ouverts et soulagés de quelques provisions. La Presse, réduite au journal « Le Trait d’Union » ne véhiculait que des idées collaborationnistes et terminait dans les latrines alors que « l’Optimiste » le canard du stalag, était lu attentivement par chaque prisonnier.</p><br>
