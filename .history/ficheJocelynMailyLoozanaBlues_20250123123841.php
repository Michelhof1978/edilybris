                    —  Quoi qu’vous voulez ? <br><br> 

Je regardai le gars qui se tenait en face de moi. Je lui répétai que j’étais venu chercher un panneau de portière pour ma Pontiac Grand Prix, <br> <br> 

— Une de 1966, parce que les modèles sont différents ensuite. J’ai vu votre publicité dans le journal, et comme vous ne faites que dans l’épave d’américaines, j’ai pensé que vous auriez cela. <br> <br> 

Le type lâcha un pet sonore. Assez petit mais trapu, engoncé dans une cotte de travail qui semblait avoir été bleue dans un passé lointain, il devait approcher la cinquantaine, et l’état de ses mains trahissait, sous l’épaisse couche de crasse noirâtre qui imprégnait chaque pore de sa peau, des heures de dur labeur mécanique. Le plus impressionnant était sans aucun doute son visage, à la fois mou et bouffi, ruiné par une solide couperose ; il s’étalait en une large face de lune grêlée, entre deux petites oreilles décollées. Un nez de boxeur en renforçait l’aspect comique, mais la surbrillance quasi surnaturelle de ses petits yeux de goret ne m’inspirait aucune confiance. Solidement campé sur deux jambes massives, il attendait tranquillement le chaland et sereinement les services d’hygiène. Compte tenu de l’heure matinale, les effluves corporels étaient encore légers, mais au regard de l’allure générale du personnage, je me félicitai de ne pas être passé le voir en fin de journée. Sa dernière douche devait dater de son dernier rasage ; étant donné l’aspect bleuté du menton, j’estimai celle-ci à trois jours. J’eus confirmation de cette évaluation lorsqu’il me prit par la manche et me souffla :<br> <br> 

— Y’en a une au fond, après le corbillard. Une 66. Tu r’gardes si y a c’que tu cherches, et tu viens me l’dire. C’est nous qu’on démonte ici, pas les merdeux qui cassent tout. <br> <br> 

Je me retirai poliment, en apnée, et lui demandai par où passer. Sans un mot, il pointa un énorme pouce crasseux à l’ongle rogné au-dessus de sa solide épaule. Il semblait épuisé d’avoir dit tant de mots d’un seul coup. Je suivis donc la direction indiquée, sous l’œil inquisiteur et suspicieux du roi de la carcasse. <br> <br> 

Je franchis d’abord une sorte de couloir constitué de blocs de ferraille compactée, une espèce d’allée métallique d’une dizaine de mètres, avec des pieux soudés au sommet, reliés par un fin réseau de barbelés. Au bout de ce corridor digne d’une figuration de jeu vidéo, s’étendait le cimetière automobile proprement dit. J’avançai précautionneusement, cherchant à repérer ce fameux corbillard. De prime abord, l’ensemble était presque organisé ; d’un côté les pneus, les moteurs et organes de transmission, démontés et remisés sous des hangars soutenus par des piliers contre lesquels je n’aurais pas osé soulager ma vessie de peur de voir tout s’effondrer. De l’autre, gisaient des amas de portières, de capots, les carcasses numérotées posées sur des jantes, parfois même recouvertes d’une bâche. Mais plus loin, c’était moins clair, les modèles étaient mélangés et l’état général plus proche de l’abandon que de la conservation. Je marchai prudemment, mais j’étais  attiré par l’arête aérienne d’une aile de Chevy Impala 58, par la lourde calandre cubique d’une Buick Road master, ou par le galbe arrondi du toit d’une Plymouth 40. <br> <br> 

Il y a quelque chose à la fois de magique et d’effrayant dans les casses...<br> <br> 


