    <h2 class="h2Jaune">Luc LE GUENNEC dit "deuzeLLGé"</h2>
<div class="text-center m-5">
    <p>Guy Lo Pinto, petit-fils de siciliens immigrés, est né en Tunisie. De quoi alimenter dès son plus jeune âge son goût pour la lumière et la couleur.</p><br>

    <p>Enfant déjà, il passait beaucoup plus son temps à scruter le ciel et la mer pour y chercher un sens, des mouvements, des nuances, des signes spirituels et cosmiques, plutôt qu’à s’intéresser aux formes concrètes de la réalité qui l’entourait.</p><br>

    <p>Il fait des études primaires et une partie des secondaires au Lycée Carnot de Tunis, établissement d’enseignement français, qui le fai baigner dans la culture francophone, l’initie à la beauté et aux subtilités de la langue de Molière. Ainsi Guy découvre la richesse de ce patrimoine littéraire.</p><br>

    <p>Il en garde un goût prononcé pour l’écriture.</p><br>

    <p>Il bénéficie également de la richesse du terreau tunisien où les différentes cultures et religions cohabitent, s’enrichissent mutuellement et alimentent sa curiosité spirituelle. Mais il ressent aussi une sorte d’écartèlement douloureux entre ces cultures.</p><br>


    <p>Sa famille étant dans l’obligation de quitter la Tunisie au moment de son indépendance, il éprouve son premier moment de déchirure qui le poursuivra pendant toute son existence. Certains de ses poèmes illustreront cette souffrance.</p><br>
    <p>Guy Lo Pinto débarque à Paris, passe son baccalauréat. Il obtient un diplôme de droit et de sciences politiques.</p><br>

    <p>Ses compétences l’amènent naturellement à entamer une profession de juriste puis de juriste financier dans divers établissements bancaires. Cette profession qui nécessite des aptitudes de rigueur a toujours été jugée par lui comme antinomique avec son goût pour la beauté, l’intime, le rêve, l’évasion.</p><br>


    <p>Pendant ses moments de loisirs, il dévore les œuvres de poètes aussi différents que Rimbaud, Baudelaire, Neruda, Rilke, Éluard, René Char, mais aussi celles des poètes de l’Italie chère à son cœur, comme Buffalino ou Cristina Campo.</p><br>

    <p>Parallèlement, il ne cesse de s’intéresser aux techniques picturales abstraites au travers notamment de peintres comme Kandinsky, Pollock, ou Rothko.</p><br>

    <p>Il remplit également des carnets de poésies personnelles dont certaines sont reprises sur le premier recueil publié par Edi’Lybris aujourd’hui.</p><br>
    <p>La peinture se concrétise un peu plus tard, notamment au moment de son déménagement sur l’île de Ré, où il a tout le loisir, la liberté, et l’espace pour s’exprimer. Rapidement il se fait connaître par de nombreuses expositions dans sa région puis à Paris et à l’étranger.</p><br>


    <p>​​​​​​​S​​​​​​​a poésie est le reflet de sa vie faite d’exaltations, de voyages, d’amours, de ruptures, de douleurs de résiliences de spiritualité.</p><br>

    <p>Il a le désir, comme en peinture, de garder sa liberté, de ne pas se faire enfermer dans les carcans de « la bien-pensance » poétique. Il se considère comme un « touche à tout », utilisant tout aussi bien la versification que la prose. Il a découvert il y a peu de temps le potentiel expressif que pouvait lui apporter le slam .</p><br>

    <p>Son art, comme il le dit, est en perpétuel mouvement. </p><br>
</p>
