— Tonnerre de Brest, qui est le chef ? s’écria-t-il.
— As ‘tu fini d’jucher d’mêm ? lui dit son père.
Les autres pierres taillées furent plus faciles à extraire et, fort satisfait de son travail, Michel s’apprêtait à remonter lorsque son pied s’enfonça. En le retirant, un morceau de toile en plastique s’accrocha à sa chaussure droite. Il se baissa et en aperçut un autre. Il tira vivement dessus, par simple curiosité. Il appela son père qui buvait tranquillement une bière en attendant la fin des opérations.<br>
— Papa, viens voir, il y a encore un chien d’enterré, c’est un vrai cimetière, où je ne m’y connais pas !
Georges, un peu agacé, n’avait pas l’intention de jouer les fossoyeurs mais l’insistance de son fils l’obligea à descendre dans ce trou qui, au départ, ne mesurait que quarante centimètres de long et maintenant, atteignait près d’un mètre soixante. Il marmonna :<br>
— Ces drôles, tous d’même, pouviant pas faire simple !
Même s’il avait conservé une bonne forme physique, sauter dans cette fosse ne l’enchantait pas et il prit certaines précautions, pour s’éviter une luxation. Il atterrit dans le fond en douceur et se retrouva collé au dos de Michel.<br>
— On dirait une toile cirée, je vois des petites fleurs de différentes couleurs. Tirons dessus, on verra bien !
Au fur et à mesure, la nappe sortait de la terre et parfois se déchirait. Les deux hommes commençaient à suer et l’odeur de la terre humide leur collait à la peau.<br>
— Eh bien dis donc, la bestiole était bien emballée, il y a encore une sorte de sac. Si ça continue, on va passer notre temps à larguer les cadavres de chiens dans les bois !<br>
— Si on z’ou laissian ! suggéra Georges, peu enclin à recommencer le même manège que la veille, après tout, o fé ren !<br>
Son fils ne semblait pas entendre et continuait de tirer sur la bâche en plastique et, subitement, il s’écria :<br>
— P’pa, regarde ! …
— Quoi encore ? maugréa Georges.
— Une main !…
Et il se retourna pour vomir, laissant son père complètement éberlué. Quelques minutes s’écoulèrent avant que les hommes ne soient en mesure de reprendre leurs esprits.<br>
— Faut sortir de là et appeler les flics ! déclara Michel.
— On n’a pas fini d’être enquiquinés ! répondit son père qui, subitement, retrouvait l’usage de la langue française.<br>
Il n’avait effectivement pas tort. Après avoir ôté leurs équipements de jardiniers, les deux hommes rentrèrent dans la maison et s’accordèrent un moment de répit avant de téléphoner. Un petit Cognac leur redonnerait du courage ! Michel appela le 17 et expliqua brièvement la situation :<br>
— Ne touchez plus à rien, on arrive ! fit son interlocuteur.<br>
— Et le saule qui n’est toujours pas planté ! se plaignit Georges…

