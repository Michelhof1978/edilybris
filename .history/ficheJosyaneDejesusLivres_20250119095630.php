        <h2 class="h2Jaune">Présentation de Josyane De Jesus-Bergey </h2>
        <div class="text-center">
  <p>
  Très attachée à cet aller vers l’autre que justifie dit-elle le poème, Josyane De Jesus-Bergey, Poète franco-portugaise, retraitée de la fonction publique est née et vit à La Rochelle (Chte-Mme, F.) Elle écrit depuis l’enfance, poésie, contes, essais, chansons (S.A.C.E.M).

Elle s’occupe de différentes associations culturelles, du Printemps des Poètes et réalise des ateliers d’écriture en différentes structures en France et à l’étranger.

Fait partie de l’équipe des animateurs du Festival » Voix Vives de Méditerranée en Méditerranée » de Sète.

Régulièrement publiée en revue, elle est traduite en arabe, espagnol, italien et portugais. Invitée à de nombreuses rencontres et festivals tant en France qu’à l’étranger, elle a publié une vingtaine de livres (voir Bio-bibliographie).
  </p>

  <h2 class="h2Jaune">Le photographe : Houari BOUCHENAK-KHELLADI </h2>

  <p>
    Et puis, dire « elle »…. !!! J’essaie avec « je ».<br>
    Au fait, je suis née le 6 juin 1954 à Niort.<br>
  </p>
 
  <div>
    <img src="images/JocelyneCathelineau/dsc01362_11zon.webp" alt="jocelyne Cathelineau" class="img-fluid w-25 m-4">
  </div>
  
  <div>
    <img src="images/JocelyneCathelineau/portrait-jocelyne-fond-modifie-1_11zon.webp" alt="jocelyne Cathelineau" class="img-fluid w-25 m-4">
  </div>
  <p>
    Juste après mes enfants et mon petit-fils, la grande affaire de ma vie a toujours été la lecture. <br>
    Une journée sans lecture, une salle d’attente sans bouquin… nuisent gravement à ma santé mentale.  <br>
    Lire et écrire sont les deux versants d’une même passion. Et j’ai toujours écrit. J’ai dû commencer vers huit ans, avec les « cahiers de souris » où je racontais en les illustrant l’histoire d’une communauté de souris. On m’a fait envoyer au même âge quelques poèmes à la revue catholique « Bernadette », je le regrette ! <br>
    Puis, j’ai eu des années des correspondants, anglais, bulgare, russe, sénégalais… tout en écrivant régulièrement au parrain de ma sœur. <br>
    Et encore à mon amie d’enfance, dès que nous étions séparées une demi-journée. J’ai tenu un journal de mon adolescence à la quarantaine… Parallèlement, j’ai écrit des nouvelles, des romans, des contes, que je me suis appliquée à ne pas signer et à perdre, voire à détruire. La trentaine a vu le début des articles, pour des revues culturelles, puis pour la presse protestante. <br>
    J’ai été correspondante de presse, onze mois.
  </p>
  <p>
    Puis il y a eu un creux… une vie professionnelle très prenante, de gros soucis, un manque de temps (comment écrire quand on passe son temps libre en voiture, à visiter sa fille dans des hôpitaux éloignés ?) sont sans doute la cause. <br>
    Plus un manque de confiance inhérent à ma situation du moment.<br>
  </p>
  <div>
    <img src="images/JocelyneCathelineau/portrait-jocelyne-fond-modifie-2_11zon.webp" alt="jocelyne Cathelineau" class="img-fluid w-25 m-4">
  </div>
  <p>
    L’inspiration est revenue, comme une vanne qu’on ouvre, à partir de 2003. Pour celui qui m’a toujours dit « Tu es capable, je te fais confiance » j’ai recommencé, en évitant d’égarer mes productions.  <br>
    Tant qu’il a été en vie, j’ai écrit au moins un livre par an, et à partir de 2010, année de naissance de mon petit-fils, non seulement j’ai arrêté de perdre ce que j’écrivais, mais j’ai signé chaque ouvrage de mon nom, qui est aussi celui de Sam.
  </p>
  <p>
    Le confinement, de nouveaux problèmes de santé qui m’ont clouée à la maison, la maladie puis la perte de mon ami n’ont pas tari la source. Écrire est mon équilibre, ma respiration, mon refuge. Éditer, je n’y pensais même pas… On m’a dit un jour « Écrire c’est le paradis, éditer c’est le purgatoire, vendre, c’est l’enfer ». <br>
    Et je trouvais mes ouvrages de « family-fiction » trop personnels. Quand j’ai fait la connaissance de Pierre et d’Edi’lybris, l’idée ne m’a pas plus effleurée. Je me suis lancée dans une activité qui compensait un peu ma peine d’avoir dû quitter l’Éducation nationale pour cause de santé et de trimestres suffisants : la relecture et la correction des auteurs de la petite maison d’édition saint-maixentaise.  Seuls les imbéciles ne changent pas d’avis, et me voilà désormais tentée par l’édition. Et projetée dans l’aventure de la souscription, en ne sachant absolument pas comment ça va se passer…
  </p>
