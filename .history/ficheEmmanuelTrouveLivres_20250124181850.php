                 <p class="lead">

                 Dans la continuité de « Entre rêve et poésie », Emmanuel Trouvé pose un regard empreint d’optimisme sur les vicissitudes du monde qui l’entourent, que ce soit à son niveau ou celui de l’actualité.

Tendresse et violence, empathie et révolte, sensibilité et colère, s’alternent sous une plume toujours empreinte d’espoir.

Poète au fond de l’âme, Emmanuel Trouvé semble traverser son existence dans une contemplation quasi onirique des choses de la vie. Ses petites histoires poétiques ne s’encombrent pas d’une prosodie parfois austère et contraire à sa liberté d’expression, elles touchent simplement le cœur des lecteurs désireux d’aller à l’essentiel.

Sans vouloir tomber dans la facilité d’un jeu de mots aisé, l’auteur de ces poésies s’est « trouvé » dans un style qui lui est propre et qu’à coup sûr, vous adopterez et apprécierez !
