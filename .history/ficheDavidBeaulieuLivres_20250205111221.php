                    <p>
                        David BEAULIEU est né le 16 septembre 1968 à Limoges. Il y grandit jusqu’à sa majorité et décroche un CAP de cuisinier.  
                        À la fin de son apprentissage, il part travailler à Londres, ce qui lui permet de se familiariser avec la langue de Shakespeare.  
                        Son métier l’amène à allier découverte culinaire et voyages : Écosse, Angleterre, Espagne, Portugal, Allemagne et l’Île de La Réunion.  

                        Plus tard, David devient cuisinier marin sur des voiliers (charters de luxe) aux Antilles pendant quatre saisons, une époque d’insouciance et de soleil.  
                        En 1998, il s'installe à La Rochelle, rencontre des marins et traverse l’Atlantique à quatre reprises en tant que second.  

                        Lors d’une transat retour sur un bateau de course (Newport - Hendaye), il a l'idée de créer une association pour la sauvegarde des océans : **ECHO-MER**, qui existe depuis plus de 20 ans.  

                        Aujourd’hui, David navigue toujours et écrit. **"Le sillage de l’encre"** est son premier recueil.  
                    
                    
