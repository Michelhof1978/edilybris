Dans Paris d’après-guerre<br>
Où toutes les espérances<br>
Ont la couleur de l'air.<br>

Quelle drôle de musique<br>
Remonte à ma mémoire <br>
Et sent bon l'encaustique<br>
De nos vieilles armoires.<br>
(ext. Paris, années 50)<br>
