<div class="text-center">
  <h4><strong class="h2Jaune mb-5">BOB LE MOLDU</strong></h4> 
        <img class="img-fluid w-50 mb-1 mt-4" src="images/pierreBrandao/bob-le-moldu.webp" alt="Image centrée">
