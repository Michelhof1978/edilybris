Concevoir un bouquin, c’est bien, mais rester dans son coin est à éviter.<br> 
On a besoin de conseils pour comprendre, apprendre, échanger, oui c’est peut-être ça le secret d’écrire, échanger et transmettre.<br> 
On écrit, on aime ce que l’on fait, on est fier quand tout est abouti, mais après ?<br> 
L’aventure commence là, quand tout est terminé, c’est le départ de l’ouvrage !</p><br>

<p class="coloryellow">Edi’lybris : Parlons du livre à paraître. Comment t’est venue l’idée d’écrire sur ce sujet ?</p> 
<p>Merci Pierre de poser cette question.<br> 
C’est tout simple.<br> 
J’ai écrit un bouquin sur mon arrière-grand-oncle né en 1876, intitulé « Le parfum des racines ».<br> 
Il raconte une vie entière à travers son journal écrit en 1946.<br> 
J’ai eu la chance de lire récemment son manuscrit avec des lettres et des mots tout en rondeur, ses anecdotes, son parcours.<br> 
J’ai raconté sa tranche d’histoire vécue en 14/18 que beaucoup ne connaissent pas, une souffrance de l’éloignement de ses racines, loin, très loin, hors de France.<br> 
Je ne savais rien de lui, encore moins de son manuscrit.<br> 
J’ai lu, j’ai aimé, j’ai bistré ses anecdotes, j’ai cherché des photos, des cartes de l’époque…<br> 
Beaucoup d’émotions à travers son histoire.<br> 
Je ne souhaitais pas qu’elle se termine dans un tiroir, perdue et effacée comme tant d’autres.<br> 
Ce livre a été primé par un comité de lecture et j’en suis très fier, non pas pour moi mais pour ce héros « familial » que j’ai découvert et qui méritait, à ma façon, de réaliser un bouquin.<br> 
Cela m’a encouragé à écrire une autre vie, une autre histoire, un autre ailleurs…<br> 
« J’ai tellement envie de t’écrire » s’inscrit dans cet accord et est tout ça à la fois.<br> 
Il s’intègre à une bonne inspiration fidèle et juste.<br> 
Voilà, c’est tout simple !<br> 
Ce n’est pas une passion, mais une envie de partager…</p><br>

<p class="coloryellow">Edi’lybris : Peux-tu résumer en quelques mots l’intrigue, sans dévoiler la fin bien sûr ?</p> 
<p>Toujours difficile de dévoiler, mais tellement tentant…<br> 
C’est, cette fois-ci, un roman historique basé sur des circonstances vécues, en 1917 et plus, en Allemagne dans un camp.<br> 
Un prisonnier comme tant d’autres, mais lui est paumé dans sa mémoire en friche, il cherche et se cherche... !<br> 
Ce n’est pas un héros, les héros n’existent pas.<br> 
Il devient, malgré lui, un fil rouge pour raconter et tenter de survivre !<br> 
À travers sa souffrance, se détache un sentiment d’attachement, presque de chaleur pour comprendre son tourment…<br> 
À travers lui, j’ai aimé mon émotion égoïstement !<br> 
Je ne sais pas si ça se dit !</p><br>

<p class="coloryellow">Edi’lybris : La couverture : du projet à l’étape finale, une aventure ?</p> 
<p>Non, ce n’est pas une aventure, mais un dessin de Jean-Louis Forain, qui me plaisait bien et correspondait bien à cette histoire.<br> 
J’ai contacté les arrière-petits-enfants de ce peintre d’époque pour avoir leur permission de publier ce dessin en couverture.<br> 
Son œuvre, sa vie, son histoire, tout est écrit pour expliquer qui il était.<br> 
Là aussi, une belle émotion d’avoir l’accord et entendre une voix d’une descendante qui défend les valeurs de son aïeul !</p><br>

<p class="coloryellow">Edi’lybris : Quel message souhaites-tu délivrer à travers ton récit ?</p> 
<p>C’est surtout un récit de partages avec un grand S.<br> 
Perso, j’avais une histoire à écrire, sans penser à un message.<br> 
Peut-être un besoin d’éclairer un peu un pan disparu, mais à mon niveau, je n’ai rien à délivrer.<br> 
Les générations se suivent et ne se ressemblent pas.<br> 
Chacun vit à son époque comme il l’entend, mais ces époques sont toujours troublées par des « quelques choses » ou des « quelqu’uns »…<br> 
L’histoire peut révéler des compréhensions de ce qui a pu se passer et comme on dit souvent : « ne pas revivre les mêmes choses ! »…<br> 
Ah bon ? On voudrait tellement que ça soit vrai !<br> 
Ah, si ! J’ai un message quand même : faisons lire et surtout aimons faire lire !</p><br>

<p class="coloryellow">Edi’lybris : Merci Joël pour t’être prêté à ce jeu, à bientôt !</p> 
<p>Merci Pierre pour cette interview !</p>

