                    Dès les premiers instants de notre amitié, c’est la poésie qui nous a reliés. Emmanuel Trouvé est un enfant lumière, il est né avec cet instinct propre aux artistes et poètes véritables,  celui de ressentir le monde, de le traduire avec son art et de le partager au plus grand nombre. Même si nous sommes tous artistes, ou créateurs (c’est ce que je défends dans mon quotidien d’artiste et thérapeute), je crois que certains êtres assument plus volontiers cet habit, pas toujours facile à porter dans notre monde digitalisé.

Les souvenirs de ce Printemps des Poètes où nous nous sommes rencontrés sont comme les cerisiers japonais qu’Emmanuel affectionne, tendres et délicats. C’est ce printemps particulier qui a scellé notre amitié, une amitié qui perdure dans le temps malgré les tempêtes et les tourments contemporains. Une amitié faite de poésies bien sûr mais aussi d’échanges épistolaires, tels ces poèmes dédiés qui racontent notre printemps et l’éclosion (La fée poésie) puis l’épanouissement du poète (Faire un pont). Emmanuel a porté haut son amour de la poésie avec un premier recueil « Entre rêve et poésie ». Ayant reçu un très bel accueil, il édite ce second opus, deux ans seulement après le premier. Il s’y dévoile davantage et pose sur le monde son regard sensible. Au travers de son filtre, le monde retrouve ses couleurs pastel et nous nous laissons bercés par la musique de ses mots. Emmanuel est inspiré par tout ce qui l’entoure et le touche : l’amour, la pureté des sentiments, les êtres chers, la nature, le Japon et ses curiosités (la geisha, le Shibari), les arts, la musique (J. Higelin en particulier), et parfois l’actualité. Un voyage en terre poésie qui souligne sa liberté d’esprit, mais aussi son regard curieux, quasi gourmand de la vie et des gens.


Les images créées, son écriture sont comme des touches de couleurs donnant vie à de vibrantes peintures. Semblant s’écouler naturellement de son cœur, ses poèmes impressionnistes nous transportent dans son univers. Sa poésie est l’expression de son âme et dans ce monde en perdition nous avons tant besoin de poètes comme Emmanuel, pour nous faire rêver, chavirer, nous exiler vers un autre monde…

Le rideau s’ouvre sur un nouveau chapitre, un nouveau recueil de poèmes pour vivre et s’évader sur son île poésie… Emmanuel, encore Merci.



