            <h2 class="h2Jaune">Biographie</h2>
            <img src="images/lydiaChauvin" class="img-fluid w-75 mb-3 d-block mx-auto" alt="Illustration">

<p class="text-center">
Si un jour vous croizez Lydia Chauvin à la sortie d'une scène de spectacle, ne lui demandez surtout pas si elle aime le théâtre ! À six ans, alors qu'elle assiste à une pièce en compagnie de sa mère, elle lui dit avec une assurance qui lui sied déjà bien à l'époque : "C'est ça que je veux faire !" Et elle s'y met ! <br>
Écode de danse, parcours atypique d'une scolarité centrée sur ce métier atypique, apprentissage de la langue pour mieux écrire ses saynètes... En clair, toute sa vie est vouée à ce noble art, à l'interprétation et à la formation de jeunes passionnés -de tout âge !-.<br>

La dernière page de cette présentation est consacrée à ses multiples activités. N'hésitez pas à vous y rendre : vous serez surpris ! Le carrousel mis à votre disposition, en bas du menu, vous permettra d'appréhender un monde graphique qui s'est nourri des inventions de Lydia. <br>
Beaucoup de fraîcheur dans le trait, de joie de vivre, de plaisir de partager.

Pourquoi en parler, me direz-vous ? La raison se veut belle : les pièces de théâtre de Lydia méritent d'avoir une existence pérenne, alors à travers le livre, le lecteur poursuivra son évasion ! Découvrez page suivante le premier volume à paraître : Recette pour spectacle... on s'en pourlèche à l'avance les babines !
</p>

