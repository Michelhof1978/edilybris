                         Il se rend régulièrement en Alabama, en Louisiane et au Mississippi.<br>
                          Le charme étrange de leurs petites bourgades endormies où rouillent les carcasses dans la moiteur de Dixieland ont su l'envoûter, au même titre que le blues, les champs de coton et leurs fantômes ... <br>
                         
                         
                    <img src="images/JocelynMailly/photo1.webp" class="img-fluid w-50 mb-3 rounded" alt="photo">
                    
               
Avec Loozana Blues, Jocelyn avait réussi un beau coup de maître ! On ne connaissait de lui que sa thèse de doctorat, "le populisme dans le sud des États-Unis au XXe siècle", thème qui lui est cher puisqu'il ressort de sa fiction "Loozana blues". Avec ce premier roman, force était de constater qu'il maîtrise l'art de la rédaction, du suspens et de l'action avec un aplomb certain pour un néophyte en écriture !<br>

Alors, quand quatre ans plus tard, l'auteur récidive avec Des os et des ailes, un "road-movie" qui trouve le cadre idyllique de l'Alabama, on ne pouvait que s'attendre à du bon ! Et c'est le cas ! Si vous recherchez de l'exotisme, de l'aventure, du suspense et de la romance, c'est ici qu'il faut marquer votre empreinte : auprès des ouvrages de Jocelyn Maily !<br>
