    <h2 class="h2Jaune">Biographie</h2>

    <p class="lead text-center">
        Le bonheur est dans l’attente, le malheur vient du hasard. <br><br>

        Considérée dès sa conception comme un parasite inutile, juste bon à être éliminé, Vika décide de s’accrocher à la vie par tous les moyens. Malheureusement, elle vient au monde dans une mauvaise famille. Ce petit paquet oublié, déplacé au gré des envies de sa génitrice, meurtri par son entourage, ne baisse pourtant pas les bras, se bat. Astucieuse, intelligente, elle s’adapte, se construit, commet des erreurs, mais les assume sans faillir.<br><br>

        Ce récit, poignant, douloureux, déchirant, parfois cruel et malsain, navigue entre prose et poésie, outils d’une souffrance peinte avec la beauté de la maladresse...<br><br>

        La lecture, volontairement désordonnée, nous en met plein le visage, comme un uppercut direct qui tient sa force d’une vérité balancée sans pudeur.<br><br>

        Toi qui as entre les mains ces feuillets brûlants, attends-toi à heurter tes inébranlables convictions !<br><br>

        <strong>David Beaulieu</strong> est né le 16 septembre 1968 à Limoges. Il y grandit jusqu’à sa majorité et décroche un CAP de cuisinier.<br><br>

        À la fin de son apprentissage, il part travailler à Londres, ce qui lui permet de se familiariser avec la langue de Shakespeare.<br><br>

        Son métier l’amène à allier découverte culinaire et voyages : l’Écosse, l’Angleterre, l’Espagne, le Portugal, l’Allemagne et l’Île de La Réunion l’accueilleront durant toute sa vie.<br><br>

        Puis, David décide de tenter l’aventure et officie comme cuisinier marin sur des voiliers (charters de luxe) aux Antilles pendant quatre saisons. Il gardera le souvenir d'une merveilleuse époque d’insouciance et de soleil !<br><br>

        En 1998, notre voyageur pose son paquetage à La Rochelle et rencontre de belles personnes avec qui il navigue. À quatre reprises, David traverse l’Atlantique en tant que second, renforçant la complicité, la solidarité et l’amitié. Il se souvient notamment des nombreux convoyages de La Rochelle en Méditerranée, dont un en particulier qui le pousse jusqu’à Venise.<br><br>

        Au cours d’une transat retour sur un bateau de course et en équipage (Newport – Hendaye), l'écho de la mer va naître. Ce message clair de l'océan lui intime de créer une association œuvrant pour la sauvegarde des océans : <strong>ECHO-MER</strong>, qui a désormais plus de vingt ans d’existence.<br><br>

        Désormais, David navigue toujours dans les pertuis, surtout au profit de sa famille. L’écriture, spontanée, fragile, sensible, l’accompagne au fil de ses pensées. <em>« Le sillage de l’encre »</em> est son premier recueil. Gageons qu’il n’en restera pas là !<br>
    </p>

