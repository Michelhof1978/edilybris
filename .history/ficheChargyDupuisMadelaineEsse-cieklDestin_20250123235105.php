Pour Christine Kunz, slameuse, la poésie est un cri murmuré, une brèche dans le quotidien. L’écriture est un refuge. Les mots, d’habitude, elle les dit ; ici, elle les livre sur le papier. 
Alors, la magie prend corps et s’empare de l’esprit du lecteur. 
Nomade, il s’évade.


    Offrez-vous ce voyage, passant, et prolongez l’imaginaire de cette artiste hors du commun, que l’on peut rencontrer à La Rochelle à l’occasion des soirées «Slamalamer». 
    Ainsi, vous ferez le tour du personnage : par le mot lu, le mot dicté, la voix sublimée. 


« Délaisser les montres les voix les regards,
Oublier les secondes les fois les retards
Accuser le coup, se libérer des « faire »
Abreuver le fou qui dort en nos déserts. »
</p>      
