        <h2 class="h2Jaune">Présentation</h2>
<div class="text-center m-3">Comme Marie l’a annoncé dans « Autres Dimensions », le 5 novembre 2009 : « Certains de mes messagers vont se réveiller, maintenant. Ils accueilleront, avec humilité, leur rôle et leur fonction. Ces messagers ne sont pas aimés plus ou moins que les autres, mais je veille sur eux de manière privilégiée. Mais je vous aime tous. » François, auparavant publié sous son nom de plume François-Rose, est l’un d’eux, un messager de la venue de l’UN. Il donne le nom à sa poésie de « Contes en Cieux ». Il est également astrologue et numérologue où son livre, s’intitule « 45 Élixirs de vie »,  informe des expériences que chacun est venu vivre, et cela basé sur sa date de naissance. C’est ce qui lui a permis de découvrir que la sienne portait le nombre « 333 » (À vous, si vous en avez la curiosité, de lire les informations données sur internet). Et si son prénom et son nom, en numérologie font 11, le nombre du messager ; en astrologie, la planète Mercure est celle qui est la plus haute, au sein de sa carte du zodiaque, et conjointe à la cuspide de la Maison X, en celle-ci avec Jupiter. Maison représentant sa fonction socio-professionnelle. Alors si l’on peut se sentir perdu, au sein de ce monde de matière (« ma » valant un tiers), notre Divinité peut nous révéler les deux tiers qui nous manquent.<br François est là pour vous les révéler à travers ses écrits.<br><br>

<p class="coloryellow text-center">Le cri du goéland<br>
Raye la plage ce matin<br>
À travers les nuages<br>
Et les remous marins.<br>
(ext. Partant de toi)</p><br>

Il croque avec la même gourmandise, les gens qu’il croise dans les trains de banlieue si longtemps pratiqués, le corps souple d’une jeune femme entrevue dans un bar ou tout le raidissement de ce vieillard assis sur ce ​banc de jardin. La grâce qui rayonne du regard d’un enfant, l’élégance naturelle d’un très beau port de tête.<br><br>

<p class="coloryellow text-center">Vous portiez un chemisier frais<br>
Une jupe à fleurs, élégante<br>
Dont la taille vous serrait de près<br>
Vous faisant la gorge accueillante<br>
(ext. Le chemisier)</p><br>

<img src="images/jeanPierreCretel/toulouse-1_11zon.webp" class="img-fluid w-25 m-3 d-block mx-auto" alt="Photo">


Et on ne peut que partager cette gourmandise-là. Ces paysages, ces personnages, sont aussi autre chose que des vers descriptifs ; ils sont vivants, ils existent, on les reconnaît. Ils sont notre environnement, nos voisins, des amis, des comme nous en ce qu’ils ont d’universel.<br>

 

Cet art de la description, teintée des couleurs vives du printemps ou flamboyantes des automnes, pousse vent arrière, vers des horizons toujours nouveaux qu’il faudra bien atteindre. Cet art lui permet donc de restituer avec délicatesse souvent, avec violence parfois, l’accent d’une vérité, d’un geste, d’une image que les gens reconnaissent. Il tient surtout, et ceci explique cela, aux regards qu’il porte sur le monde et l’écoute.<br>
 L’observation des choses qui passent est une chose commune à chacun d’entre nous mais rendre avec acuité, justesse et élégance ces même « visions » en est une autre qui s’appelle le talent.<br>

Se penser écrivain est une chose mais la vie à rebondissements de Jean-Pierre Crétel devait en décider autrement. Néanmoins, on retrouve un premier recueil incluant la période de 1959 à 1973 pour L'EMPREINTE. <br>

<img src="images/jeanPierreCretel/reims_11zon_11zon.webp" class="img-fluid w-25 m-3 d-block mx-auto" alt="Photo">


Des rencontres fortuites en 1962 avec Jean Cocteau à Paris et le compositeur interprète Jacques Douai à Cap d'Ail en 1963, l'encouragent à poursuivre son travail d'écriture.<br>

Deux nouveaux recueils voient le jour entre 1976 et 1991. Toutefois, il faudra attendre sa rencontre avec Josyane de Jésus-Bergey en 1994 pour qu'il bascule et consente à faire connaître ses textes. Dès lors, il collabore au "Moulin de poésie" de Saintes jusqu'en 2005 et participe à l'anthologie de "Charente, j'écris ton nom" préfacée par Claude Roy aux éditions "le croit vif" en 1996.<br>

Il rejoint l'Union Régionale Culturelle du Bourbonnais-Auvergne par le biais de "La forêt des 1000 poètes" qui lui ouvre aussi les pages de son anthologie aux éditions Arionis en juin 1994.<br>

L'atelier de Cognac où officie Jean Trémer le sollicite pour sa nouvelle anthologie intitulé "Migration" qui verra le jour en 1997.<br>

La qualité de ses textes l'amène en 1995 à la revue Les Nouveaux Cahiers de l'Adour à Bayonne où il croise dans les pages, Guy d'Arcangues. Les NCA font paraître ses textes dans leur revue jusqu'en 2004.<br>

Les recueils s'enchainent et pas moins de quatorze titres émergent entre 1965 et 2013. Treize nouvelles viennent divertir, avec entres autres : Entre les lignes en 1965, Portation & Edition (1994), et Les livres & Bahbka (2012), L'ambition révolue (2013) suivies par Les 5 Grands (2014) mis en scène à l'exposition de Chermignac. Trajectoire Pauline et L'inconnu du bar en 2017.<br>

Le recueil La page tournée en 2014 marque une rupture et Bouffée d'air en 2015 expriment un renouveau. Son œuvre s'étale sur plus de soixante ans et reflète la diversité, l'itinérance de son parcours et en même temps l'unité d'expression comme une ambiguïté entre signes et lumières, sons et couleurs, mots et images, donnant raison à Guy d'Arcangues pour sa qualification de "poète impressionniste".<br><br>

<p class="coloryellow text-center">Enfance, ma belle enfance<br>
Dans Paris d’après-guerre<br>
Où toutes les espérances<br>
Ont la couleur de l'air.<br><br>

Quelle drôle de musique<br>
Remonte à ma mémoire<br>
Et sent bon l'encaustique<br>
De nos vieilles armoires.<br>
(ext. Paris, années 50)</p><br>

<img src="images/jeanPierreCretel/chatel-1_11zon.webp" class="img-fluid w-25 m-3 d-block mx-auto" alt="Photo">


Les chemins de la mémoire empruntent les tracés invisibles de la création et se déclinent dans l'analogie entre les mots et les sons qu'ils produisent, les couleurs qu'ils évoquent, la lumière qu'ils diffusent. Toutes ces choses, comme en attente d'écriture. Ces évocations confèrent à l'œuvre, une permanence, une certaine intemporalité. <br>
Ce n'est pas la précision des idées, non plus que la virtuosité du langage qui forme l'image qui invite à respirer l'identité de chacun de nous, dans les limites fragiles d'un souvenir, d'une sensation, d'un plaisir ou d'une émotion. <br>
Ces rencontres formelles, inattendues, parfois troublantes par ce qu'elles éveillent, nous invitent encore sans doute à aller chercher plus loin et plus profondément, ce qui motive et contient cette création, à la toucher du bout des doigts pour mieux encore la singulariser. Il écrivait récemment :

" La confrontation avec d'autres écrivains ou poètes, fait surgir en moi le désir de créer. Les mots fusent en un feu d'artifice, jaillissent d'assemblages quelque peu improbables ou d'éléments inattendus; cela féconde l'ensemble d'une réalité qui se conjugue comme un bouquet de fleurs. <br>
Une manière plus fluide de voir et de penser, de saisir autre chose." <br>          

"j'écris des vers pour évoquer mes époques et d'autres choses en d'autres vers. Je ne suis pas écrivain, je fais la poésie, les vibrations du vent, je fais la douleur et la douceur, je fais la lumière et la couleur des mots".<br>

                             Guy d'ARCANGUES
