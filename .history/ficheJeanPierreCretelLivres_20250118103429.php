                    <p>Guy d’Arcangues en disait toute sa sympathie, qualifiant l’œuvre de J.-P. Crétel comme « poète impressionniste » pas meilleur qu’un autre, mais écrivant comme personne. Affirmons donc sans ambages qu’un excellent poète écrit, poétise comme aucun autre.</p>

                    <p>Depuis 1959, cette mer de doute toujours recommencée baigne les plages de son perpétuel questionnement. Comment faire ressentir la véracité vivante d’une lumière, d’un son, d’un lieu, d’une époque ?</p>

                    <p><strong class="coloryellow">
                        Cri du goéland<br>
                        Raye la plage ce matin<br>
                        À travers les nuages<br>
                        Et les remous marins.<br>
                        (ext. Partant de toi)
                    </strong></p>

                    <p>Ces paysages, ces personnages, sont autre chose que des vers descriptifs ; ils sont vivants, ils existent, on les reconnaît. Ils sont notre environnement, nos voisins, des amis, des comme nous en ce qu’ils ont d’universel.</p>

                    <p><strong class="coloryellow">
                        Vous portiez un chemisier frais<br>
                        Une jupe à fleurs, élégante<br>
                        Dont la taille vous serrait de près<br>
                        Vous faisant la gorge accueillante<br>
                        (ext. Le chemisier)
                    </strong></p>

                    <p>Les chemins de la mémoire empruntent les tracés invisibles de la création. Ces évocations confèrent à l'œuvre une permanence et une intemporalité.</p>

