    <h2 class="h2Jaune">Luc LE GUENNEC dit "deuzeLLGé"</h2>
<div class="text-center m-5">
    <p>Guy Lo Pinto, petit-fils de siciliens immigrés, est né en Tunisie. De quoi alimenter dès son plus jeune âge son goût pour la lumière et la couleur.

Enfant déjà, il passait beaucoup plus son temps à scruter le ciel et la mer pour y chercher un sens, des mouvements, des nuances, des signes spirituels et cosmiques, plutôt qu’à s’intéresser aux formes concrètes de la réalité qui l’entourait.

Il fait des études primaires et une partie des secondaires au Lycée Carnot de Tunis, établissement d’enseignement français, qui le fai baigner dans la culture francophone, l’initie à la beauté et aux subtilités de la langue de Molière. Ainsi Guy découvre la richesse de ce patrimoine littéraire.

Il en garde un goût prononcé pour l’écriture.

Il bénéficie également de la richesse du terreau tunisien où les différentes cultures et religions cohabitent, s’enrichissent mutuellement et alimentent sa curiosité spirituelle. Mais il ressent aussi une sorte d’écartèlement douloureux entre ces cultures.


Sa famille étant dans l’obligation de quitter la Tunisie au moment de son indépendance, il éprouve son premier moment de déchirure qui le poursuivra pendant toute son existence. Certains de ses poèmes illustreront cette souffrance.
    </p>
