 <script>
    // cartes avec bouton next previous
    let currentGroup = 0; // Index du groupe actuel (commence à 0)

// Fonction pour afficher le groupe courant
function showGroup(direction) {
    const groups = document.querySelectorAll('.book-group');

    // Masquer le groupe actuel
    groups[currentGroup].classList.remove('active');

    // Calculer le nouvel index basé sur la direction
    if (direction === 'next') {
        currentGroup = (currentGroup + 1) % groups.length; // Aller au suivant, revenir au premier à la fin
    } else if (direction === 'prev') {
        currentGroup = (currentGroup - 1 + groups.length) % groups.length; // Aller au précédent, revenir au dernier si on dépasse le début
    }

    // Afficher le nouveau groupe
    groups[currentGroup].classList.add('active');
}

// Initialisation : Afficher uniquement le premier groupe
document.addEventListener('DOMContentLoaded', () => {
    const groups = document.querySelectorAll('.book-group');
    if (groups.length > 0) {
        groups[currentGroup].classList.add('active');
    }
});

</script>
