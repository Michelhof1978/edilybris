            <h2 class="h2Jaune">Une femme poète à la Rochelle</h2>
            <img src="images/christinekunz/photo1.webp" class="rounded w-25 img-fluid m-3 d-block mx-auto" alt="Photo de Christine Kunz">

            <p class="text-center">
                Rentrer dans l'univers de Christine KUNZ s'avère à la fois enthousiasmant et intrigant : d'une part à cause de son énergie débordante qui nous pousse à franchir les portes entrouvertes de son imaginaire, et d'autre part pour ce mystère jalousement préservé de sa vie familiale.<br>

                Parlons donc passion avant tout : l'écriture, qu'elle décrit comme intime, spontanée et thérapeutique, se nourrit des vicissitudes de la société, des injustices et des intolérances des peuples dans leur capacité à vivre en harmonie, de la substance même de l'homme à aimer son prochain lorsqu'il s'agit de désir et de partage, de l'emprise de la musique sur nos habitudes de vie. La liste est loin d'être exhaustive, mais permettra au lecteur de mieux cerner notre nouvelle adhérente.<br>

                Parlons aussi de l'expression de ses passions, en commençant par sa rencontre en 2005 avec le slam. Pour Christine, ce fut révélateur, comme l'opportunité de faire "sauter le verrou qui interdisait d'être, de dire, de s'affirmer". Alors, s'enchaînent plusieurs productions, sous l'égide de la première scène ouverte de slam poésie à la Rochelle, au Café des Deux Tours, appelée SLAMALAMER.<br>

                <img src="images/christinekunz/photo2.webp" class="rounded w-25 img-fluid m-3 d-block mx-auto" alt="Photo de Christine Kunz">

                Sa présence est également remarquée à plusieurs reprises :
                <ul>
                    <li>En 2008, à l'inauguration de l'église Saint-Sauveur et en off à l'occasion des Francofolies de La Rochelle.</li>
                    <li>En 2010, au festival de l'université de La Rochelle, dans le cadre des "Étudiants à l'Affiche".</li>
                    <li>En 2011, aux Assises de la Culture à Niort.</li>
                    <li>En 2012, au Festival Springtime Delight et en workshop chez Strate College (école de design).</li>
                    <li>En 2013, interventions régulières dans le milieu scolaire (primaire et secondaire).</li>
                    <li>En 2014 et 2015, animation d'ateliers d'expression sur le métissage au Museum d'Histoire naturelle de La Rochelle.</li>
                </ul>

                <img src="images/christinekunz/photo3.webp" class="rounded w-25 img-fluid m-3 d-block mx-auto" alt="Photo de Christine Kunz">

                <ul>
                    <li>En 2016, création avec deux partenaires du festival "Vibrations poétiques", dont la première édition au Cloître des Ursulines à La Rochelle est un succès.</li>
                    <li>En 2017, collaborations multiples : museum d'Histoire Naturelle, médiathèque Michel Crépeau, collège de l'Atlantique, réseau scolaire de Mireuil. Le projet "STAND UP" consacre son sens de la scène avec un atelier slam au salon du livre féminin et une participation au salon du Livre et des Droits de l'Homme à Saintes.</li>
                    <li>En 2018, projet "Paroles d'Humains" en partenariat avec le collectif "Actions Solidaires". Christine s'associe également avec Frank Nelson pour créer "Big Bass Band".</li>
                </ul>

                En 2019, l'idée de transposer ses textes en un recueil se concrétise grâce à Pierre Brandao. Une aventure éditoriale commence avec "Abreuver le fou qui dort en nos déserts".
