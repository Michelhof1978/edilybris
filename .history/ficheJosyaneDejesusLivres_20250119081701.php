    Très attachée à cet aller vers l’autre que justifie dit-elle le poème, Josyane De Jesus-Bergey, Poète franco-portugaise, retraitée de la fonction publique est née et vit à La Rochelle (Chte-Mme, F.) Elle écrit depuis l’enfance, poésie, contes, essais, chansons (S.A.C.E.M).<br>

Elle s’occupe de différentes associations culturelles, du Printemps des Poètes et réalise des ateliers d’écriture en différentes structures en France et à l’étranger.<br>

Fait partie de l’équipe des animateurs du Festival » Voix Vives de Méditerranée en Méditerranée » de Sète.<br>

Régulièrement publiée en revue, elle est traduite en arabe, espagnol, italien et portugais. <br>*Invitée à de nombreuses rencontres et festivals tant en France qu’à l’étranger, elle a publié une vingtaine de livres (voir Bio-bibliographie).<br>

 
