 <script>
  let currentGroupIndex = 0; // Index du groupe actuellement visible
  const bookGroups = document.querySelectorAll('.book-group'); // Tous les groupes

  function showGroup(direction) {
    // Masquer le groupe actuel
    bookGroups[currentGroupIndex].classList.add('hidden');

    // Calculer l'index du nouveau groupe
    if (direction === 'next') {
      currentGroupIndex = (currentGroupIndex + 1) % bookGroups.length;
    } else if (direction === 'prev') {
      currentGroupIndex = (currentGroupIndex - 1 + bookGroups.length) % bookGroups.length;
    }

    // Afficher le nouveau groupe
    bookGroups[currentGroupIndex].classList.remove('hidden');
  }

  // Initialisation : afficher uniquement le premier groupe
  bookGroups.forEach((group, index) => {
    if (index !== currentGroupIndex) {
      group.classList.add('hidden');
    }
  });
</script>

