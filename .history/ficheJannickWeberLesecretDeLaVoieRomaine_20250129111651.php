<h4 class="h2Jaune text-center m-3 ">CHAPITRE 2</h4>

<p class=" lead text-center">Jean était un homme heureux. Il vivait dans un bourg de Charente-Maritime, plus particulièrement en Saintonge, et rien ni personne n’aurait pu le persuader de changer de région. Pour lui, une nouvelle vie en Espagne ou au Portugal n’était pas prévue au programme. Il dirigeait d’une main de fer une entreprise de menuiserie de cinquante-cinq employés et, depuis ces dernières années, il s’était introduit dans les milieux ouverts aux marchés publics. Il n’était pas né philanthrope et s’enorgueillissait de ce trait de caractère qu’il tenait, aux dires de sa mère, d’un de ses aïeux, réputé pour son côté mercantile. Depuis son élection en qualité de président de l’organisation patronale de l’arrondissement, il participait à bon nombre de manifestations et commençait à s’intégrer dans la petite bourgeoisie locale.  <br>

Lorsqu’il se penchait sur son existence, ses pensées se portaient en premier lieu sur son entreprise. Venaient ensuite sa femme Gisèle, sa fille Justine et son chien Roquet. Donc, rien d’exceptionnel, si ce n’est un réel égoïsme, mais il en assumait les conséquences. Il n’ignorait pas que les deux donzelles le qualifiaient de narcissique, d’égocentrique et de prétentieux, mais qu’importe, il aimait plaire et pérorer. Ses relations d’affaires jouaient les hypocrites en le gratifiant de sourires alors qu’elles ne se gênaient pas pour le critiquer. Doté d’une ouïe particulièrement bien développée, il les entendait marmonner :<br>
— Il oublie d’où il sort, celui-là ! Son père était vidangeur…<br>

« Quels sournois ! Tous des faux-culs ! » pensait-il, tout en espérant qu’un jour, il pourrait les arnaquer. <br>

Il avait intégré l’entreprise à l’âge de seize ans pour y effectuer son apprentissage et ne l’avait plus quittée. Gisèle, la fille du patron d’alors, venait de fêter ses dix-huit printemps. Sans être particulièrement jolie, elle arborait un minois agréable, des formes généreuses et, surtout, s’avérait comme un parti fort intéressant. Il avait compris très tôt que l’argent faisait tourner le monde, et que ce n’était pas en restant simple ouvrier qu’il pourrait mener la vie dont il rêvait. Celle de ses parents était d’une désespérante tristesse et il voulait, à n’importe quel prix, échapper à cette médiocrité. Son père travaillait comme vianus, et dès qu’il franchissait la porte, la famille devait supporter ses relents puants. Ce n’était pas un adepte farouche des douches, et les désagréments olfactifs intégraient leur quotidien. Afin d’arrondir les fins de mois, sa mère, sans formation professionnelle, effectuait bon nombre d’heures de ménage. Ils occupaient une maisonnette au cœur d’un village situé aux Essards, à proximité immédiate de deux fermes où l’ensilage des terres rendait l’air irrespirable. Bien que modeste, leur habitation était dotée du confort minimal avec l’électricité, l’eau chaude, le chauffage central et le téléphone. <br>

Fils unique, il avait fréquenté l’école primaire puis le collège. C’était un élève au quotient intellectuel moyen et, une fois son brevet obtenu, son professeur principal lui avait suggéré d’opter pour un métier. 

Un des employeurs de sa mère dirigeait une menuiserie à Romegoux, et c’est de cette manière qu’il s’introduisit dans la famille Rouquin.

Sa morphologie assez imposante pour un garçon de son âge, ses yeux verts et ses cheveux bouclés châtains, lui valaient déjà les attentions du sexe féminin. Gisèle, de deux ans sa cadette le regardait en douce sans oser s’en approcher. Il est vrai que ses parents veillaient sur sa vertu et ne voyaient pas l’intérêt d’une idylle avec un jeune, juste sorti de l’adolescence, sans réelle instruction et surtout sans benace. Il faut rappeler qu’à l’époque, malgré l’évolution de la société, les idées ancestrales liées à la possession de biens restaient toujours ancrées, surtout en milieu rural. En réfléchissant bien, cela n’a guère changé !   <br>

Même s’il n’avait pas eu le choix, il aimait travailler le bois, matériau noble par excellence avec l’avantage de ne pas dégager de mauvaises odeurs. Il préparait un CAP « menuiserie fabricant » et partageait son temps entre la pratique, et la théorie au lycée technique. Son salaire d’alors s’élevait à huit cent trente francs, une somme non négligeable que ses parents avaient accepté de lui laisser en totalité tout en lui faisant observer que d’être logé et nourri représentait pour lui une sérieuse économie. C’est donc fou de joie qu’il utilisât ses premières payes pour l’acquisition d’un scooter. À partir de ce moment-là, terminé la bicyclette, les séances de pleurnicherie auprès de son père pour qu’il l’emmène dans un endroit et les attentes interminables dans les abris-bus. Il sillonnait allègrement les routes de la région pour retrouver ses amis (es). Comme tous les jeunes de son âge, il fréquentait les boîtes de nuit, les cinémas et les cafés, mais restait attentif quant à la consommation d’alcool. Il conservait en mémoire la réaction de son « vieux » lorsqu’un soir, ayant abusé de la bière, il était rentré à la maison, l’œil éteint et la démarche hésitante.<br>
— Tu vois ce gourdin, c’est du chêne, du solide. La prochaine fois que je te trouverai dans cet état-là, je t’assommerai.<br>

Il l’avait ensuite emmené dans la salle de bains et l’avait plongé tout habillé dans la baignoire en lui maintenant la tête sous l’eau. Heureusement, la mère était intervenue pour mettre fin à cette punition mémorable.<br>

À dix-huit ans, son examen en poche, et faute d’avoir obtenu l’assentiment de ses parents, il renonça au baccalauréat professionnel. Son père lui avait fait observer que c’était un investissement inutile en lui précisant :<br>
— Si tu sais mener ta barque, tu t’en sortiras tout seul. Pas nécessaire d’user tes chulottes sur les bancs de l’école ! <br>

Huit jours après cet entretien familial, le patron de la menuiserie, Jules Rouquin l’avait convoqué, et il se souvenait encore des termes de son rendez-vous. <br>
— Mon gars, tu viens de terminer ton apprentissage. Comme tout s’est bien passé et que j’ai besoin de personnel, je peux t’embaucher en qualité d’ouvrier d’exécution. Si tu acceptes, pour le salaire, tu verras avec la secrétaire-comptable.<br>

Jean avait donné son accord, trop heureux de trouver un travail aussi rapidement.</p><br>
