<h4 class="h2Jaune text-center m-3 ">EXTRAIT CHAPITRE 2</h4>


<p class=" lead text-center">Après cette échappée chalonnaise, la vie reprend son cours, et Guyllot s’attèle à sa tâche. Entre les affaires de drogue, d’infractions à la législation, le travail clandestin, les délits de garde de mineurs, et les incivilités diverses, il ne chôme pas.<br>

Saint-Savinien compte deux mille cinq cents âmes, traversée par la Charente et par le Bramerit, le Bertet et le Charenton. Le dernier meurtre remonte à plusieurs années et Guyllot s’accommode fort bien de cette relative tranquillité.<br>

Le samedi 10 août 2019, en début d’après-midi, de Saint-Saturnin de Séchaud, le planton reçoit un appel téléphonique d’un habitant complètement affolé :<br>
— J’ai trouvé un mort à côté de l’église.<br>
— Ne touchez à rien, on arrive.
— J’ai prévenu les pompiers.<br>
Le major immédiatement informé et l’adjudant-chef Gérald sautent dans la Mégane RS. Sirène hurlante, ils se dirigent vers ce village rattaché à Port d’Envaux. Il demande à Florian, le technicien en investigation criminelle de proximité (T.I.C.P.) de les rejoindre, en compagnie d’un monogalon. Cette seconde voiture, de même marque que la précédente, mais plus ancienne, roule moins vite. Le conducteur marmonne :<br>
— Quand recevrons-nous une nouvelle bagnole ? Cela devient désespérant.
— C’est partout pareil, lui répond son collègue. Et encore, il ne faut pas se plaindre, on est mieux loti que la justice.<br>
— Comment le sais-tu ?<br>
— Mon parrain bosse dans un tribunal. Impossible d’utiliser son imprimante, faute de toner.<br>
Le bruit strident attire l’attention des véhicules et des cyclistes qui circulent sur la route départementale. Chacun se pousse pour laisser la priorité à ces voitures bleues, suspectant un problème important.<br>
Dix minutes plus tard, ils stoppent devant l’église de Saint-Saturnin, à côté d’une mobylette qui tracte une remorque. Presque aussitôt, les sapeurs-pompiers arrivent également à grand renfort de deux tons. Guyllot aperçoit un individu en tenue de travail qui lui adresse de grands signes.<br>
— Venez, c’est ici. Quelle horreur !<br>
— Calmez-vous, nous allons gérer la situation.<br>
Les gendarmes le suivent, tournent à droite et descendent quelques marches. Sur leur gauche, le long du mur, ils voient un corps recroquevillé, allongé sur le sol.</p><br>

