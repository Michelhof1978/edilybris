                    <p class="lead text-white text-center m-4">
                    Constamment dans le doute de son talent, rarement pleinement satisfait de ses vers, Jean-Pierre Crétel, d'une écriture patiente, élabore son cheminement poétique.

 

<p>Guy d’Arcangues en disait toute sa sympathie, qualifiant l’œuvre de J.-P. Crétel comme « poète impressionniste » pas meilleur qu’un autre, mais écrivant comme personne. Affirmons donc sans ambages qu’un excellent poète écrit, poétise comme aucun autre.
</p>
 

<p></p>Depuis 1959, cette mer de doute toujours recommencée, baigne les plages de son perpétuel questionnement. Comment faire ressentir la véracité vivante d’une lumière, d’un son, d’un lieu, d’une époque ? Comment faire surgir, dans le cerveau du lecteur, l’image qu’il est nécessaire de bâtir pour bien ancrer son récit dans l’espace et le temps ?

<p></p> J.-P. Crétel reconnaît quand même que dans ce tumulte de mer, il lui arrive de décrire les sensations qu’offre, généreuse, la nature, le visage des villages où s’exprime le simple.<br>

<p></p><strong class="coloryellow">
     cri du goéland<br>
Raye la plage ce matin<br>
À travers les nuages<br>
Et les remous marins.<br>
(ext. Partant de toi)</strong><br>

<p></p> Il croque avec la même gourmandise, les gens qu’il croise dans les trains de banlieue si longtemps pratiqués, le corps souple d’une jeune femme entrevue dans un bar ou tout le raidissement de ce vieillard assis sur ce ​banc de jardin. La grâce qui rayonne du regard d’un enfant, l’élégance naturelle d’un très beau port de tête.<br>

<p></p><strong class="coloryellow">Vous portiez un chemisier frais<br>
Une jupe à fleurs, élégante<br>
Dont la taille vous serrait de près<br>
Vous faisant la gorge accueillante<br>
(ext. Le chemisier)</strong><br>

 

<p>Et on ne peut que partager cette gourmandise-là. Ces paysages, ces personnages, sont aussi autre chose que des vers descriptifs ; ils sont vivants, ils existent, on les reconnaît. Ils sont notre environnement, nos voisins, des amis, des comme nous en ce qu’ils ont d’universel.</p>

 

<p>Cet art de la description, teintée des couleurs vives du printemps ou flamboyantes des automnes, pousse vent arrière, vers des horizons toujours nouveaux qu’il faudra bien atteindre. Cet art lui permet donc de restituer avec délicatesse souvent, avec violence parfois, l’accent d’une vérité, d’un geste, d’une image que les gens reconnaissent. Il tient surtout, et ceci explique cela, aux regards qu’il porte sur le monde et l’écoute. L’observation des choses qui passent est une chose commune à chacun d’entre nous mais rendre avec acuité, justesse et élégance ces même « visions » en est une autre qui s’appelle le talent.</p>

<p>Se penser écrivain est une chose mais la vie à rebondissements de Jean-Pierre Crétel devait en décider autrement. Néanmoins, on retrouve un premier recueil incluant la période de 1959 à 1973 pour L'EMPREINTE. </p>

<p>Des rencontres fortuites en 1962 avec Jean Cocteau à Paris et le compositeur interprète Jacques Douai à Cap d'Ail en 1963, l'encouragent à poursuivre son travail d'écriture.</p>

<p>Deux nouveaux recueils voient le jour entre 1976 et 1991. Toutefois, il faudra attendre sa rencontre avec Josyane de Jésus-Bergey en 1994 pour qu'il bascule et consente à faire connaître ses textes. Dès lors, il collabore au "Moulin de poésie" de Saintes jusqu'en 2005 et participe à l'anthologie de "Charente, j'écris ton nom" préfacée par Claude Roy aux éditions "le croit vif" en 1996.</p>

<p>Il rejoint l'Union Régionale Culturelle du Bourbonnais-Auvergne par le biais de "La forêt des 1000 poètes" qui lui ouvre aussi les pages de son anthologie aux éditions Arionis en juin 1994.</p>

<p>L'atelier de Cognac où officie Jean Trémer le sollicite pour sa nouvelle anthologie intitulé "Migration" qui verra le jour en 1997.</p>

<p>La qualité de ses textes l'amène en 1995 à la revue Les Nouveaux Cahiers de l'Adour à Bayonne où il croise dans les pages, Guy d'Arcangues. Les NCA font paraître ses textes dans leur revue jusqu'en 2004.</p>

<p>Les recueils s'enchainent et pas moins de quatorze titres émergent entre 1965 et 2013. Treize nouvelles viennent divertir, avec entres autres : Entre les lignes en 1965, Portation & Edition (1994), et Les livres & Bahbka (2012), L'ambition révolue (2013) suivies par Les 5 Grands (2014) mis en scène à l'exposition de Chermignac. Trajectoire Pauline et L'inconnu du bar en 2017.</p>

<p>Le recueil La page tournée en 2014 marque une rupture et Bouffée d'air en 2015 expriment un renouveau. Son œuvre s'étale sur plus de soixante ans et reflète la diversité, l'itinérance de son parcours et en même temps l'unité d'expression comme une ambiguïté entre signes et lumières, sons et couleurs, mots et images, donnant raison à Guy d'Arcangues pour sa qualification de "poète impressionniste".<br></p>

<p><strong class="coloryellow">Enfance, ma belle enfance<br>
Dans Paris d’après-guerre<br>
Où toutes les espérances<br>
Ont la couleur de l'air.<br>

Quelle drôle de musique<br>
Remonte à ma mémoire <br>
Et sent bon l'encaustique<br>
De nos vieilles armoires.<br>
(ext. Paris, années 50)<br>
</strong></p>

<p>Les chemins de la mémoire empruntent les tracés invisibles de la création et se déclinent dans l'analogie entre les mots et les sons qu'ils produisent, les couleurs qu'ils évoquent, la lumière qu'ils diffusent. Toutes ces choses, comme en attente d'écriture. Ces évocations confèrent à l'œuvre, une permanence, une certaine intemporalité. Ce n'est pas la précision des idées, non plus que la virtuosité du langage qui forme l'image qui invite à respirer l'identité de chacun de nous, dans les limites fragiles d'un souvenir, d'une sensation, d'un plaisir ou d'une émotion. Ces rencontres formelles, inattendues, parfois troublantes par ce qu'elles éveillent, nous invitent encore sans doute à aller chercher plus loin et plus profondément, ce qui motive et contient cette création, à la toucher du bout des doigts pour mieux encore la singulariser. Il écrivait récemment :</p>

    <p>" La confrontation avec d'autres écrivains ou poètes, fait surgir en moi le désir de créer. Les mots fusent en un feu d'artifice, jaillissent d'assemblages quelque peu improbables ou d'éléments inattendus; cela féconde l'ensemble d'une réalité qui se conjugue comme un bouquet de fleurs. Une manière plus fluide de voir et de penser, de saisir autre chose." </strong>          </p><strong class="coloryellow">

    <p>"j'écris des vers pour évoquer mes époques et d'autres choses en d'autres vers. Je ne suis pas écrivain, je fais la poésie, les vibrations du vent, je fais la douleur et la douceur, je fais la lumière et la couleur des mots".</strong>  </p><strong class="coloryellow">

    <p>Guy d'ARCANGUES</p> 
                    </p>
